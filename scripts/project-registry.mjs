import { access, mkdir, readdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import process from 'node:process'
import { execFile } from 'node:child_process'
import { promisify } from 'node:util'

const execFileAsync = promisify(execFile)
const PROJECT_INFO_FILE = 'PROJECT_INFO.md'

const rootDir = path.resolve(process.cwd(), '..', '..')
const outputPath = path.join(process.cwd(), 'public', 'data', 'projects.json')

export const sourceDirs = [
  { key: 'Tool', absolutePath: path.join(rootDir, 'Tool'), defaultType: 'Tool' },
  { key: 'Web App', absolutePath: path.join(rootDir, 'Web App'), defaultType: 'Web App' },
  {
    key: 'Extension',
    absolutePath: path.join(rootDir, 'Extension'),
    defaultType: 'Extension',
  },
  {
    key: 'App Script',
    absolutePath: path.join(rootDir, 'App Script'),
    defaultType: 'App Script',
  },
]

function normalizeId(sourceKey, folderName) {
  return `${sourceKey}-${folderName}`
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '')
}

function inferTech(packageJson) {
  const deps = {
    ...(packageJson.dependencies ?? {}),
    ...(packageJson.devDependencies ?? {}),
  }
  const tech = []
  if (deps.electron) tech.push('Electron')
  if (deps.react) tech.push('React')
  if (deps.vite) tech.push('Vite')
  if (deps.typescript) tech.push('TypeScript')
  if (deps.next) tech.push('Next.js')
  if (deps.vue) tech.push('Vue')
  if (deps.svelte) tech.push('Svelte')
  if (deps['playwright-core'] || deps.playwright) tech.push('Playwright')
  return tech.length > 0 ? tech.join(' + ') : 'Unknown'
}

async function pathExists(targetPath) {
  try {
    await access(targetPath)
    return true
  } catch {
    return false
  }
}

function extractBullets(content, headingRegex) {
  const lines = content.split(/\r?\n/)
  const features = []
  let inSection = false
  for (const line of lines) {
    if (/^##\s+/.test(line)) {
      inSection = headingRegex.test(line)
      continue
    }
    if (!inSection) continue
    const match = line.match(/^\s*-\s+(.+)$/)
    if (match?.[1]) {
      features.push(match[1].trim())
    }
  }
  return features
}

function extractVersionFromMarkdown(content) {
  const versionMatch = content.match(/\b(v?\d+\.\d+\.\d+)\b/)
  return versionMatch?.[1] ?? null
}

async function safeRead(targetPath) {
  try {
    return await readFile(targetPath, 'utf-8')
  } catch {
    return null
  }
}

async function runGit(projectPath, args) {
  try {
    const { stdout } = await execFileAsync('git', ['-C', projectPath, ...args], {
      windowsHide: true,
      maxBuffer: 1024 * 1024 * 8,
    })
    return stdout.trim()
  } catch {
    return null
  }
}

async function buildGitInfo(projectPath) {
  const inside = await runGit(projectPath, ['rev-parse', '--is-inside-work-tree'])
  if (inside !== 'true') {
    return {
      isRepo: false,
      branch: null,
      summary: null,
      remote: null,
      lastCommit: null,
    }
  }

  const branch = await runGit(projectPath, ['rev-parse', '--abbrev-ref', 'HEAD'])
  const summary = await runGit(projectPath, ['status', '--short', '--branch'])
  const remote = await runGit(projectPath, ['remote', 'get-url', 'origin'])
  const lastCommit = await runGit(projectPath, ['log', '-1', '--pretty=%h %s'])
  return {
    isRepo: true,
    branch,
    summary,
    remote,
    lastCommit,
  }
}

async function ensureProjectInfo(projectPath, projectName) {
  const projectInfoPath = path.join(projectPath, PROJECT_INFO_FILE)
  if (await pathExists(projectInfoPath)) {
    return projectInfoPath
  }

  const content = `# ${projectName} Project Info

## Current Features

- TODO: Define core features for this project.

## Notes

- Generated by Tool Control Center sync.
`
  await writeFile(projectInfoPath, content, 'utf-8')
  return projectInfoPath
}

async function readProjectMetadata(projectPath, folderName) {
  const packagePath = path.join(projectPath, 'package.json')
  const readmePath = path.join(projectPath, 'README.md')
  const contextPath = path.join(projectPath, 'PROJECT_CONTEXT.md')
  const infoPath = path.join(projectPath, PROJECT_INFO_FILE)

  const packageRaw = await safeRead(packagePath)
  const packageJson = packageRaw ? JSON.parse(packageRaw) : null
  const readme = await safeRead(readmePath)
  const projectContext = await safeRead(contextPath)
  let projectInfo = await safeRead(infoPath)

  if (!readme && !projectContext && !projectInfo) {
    await ensureProjectInfo(projectPath, folderName)
    projectInfo = await safeRead(infoPath)
  }

  const features = [
    ...extractBullets(readme ?? '', /^##\s+(current features|features)/i),
    ...extractBullets(projectContext ?? '', /^##\s+current features/i),
    ...extractBullets(projectInfo ?? '', /^##\s+current features/i),
  ]

  const dedupFeatures = Array.from(new Set(features)).slice(0, 12)
  const git = await buildGitInfo(projectPath)

  return {
    packageJson,
    version:
      packageJson?.version ??
      extractVersionFromMarkdown(readme ?? '') ??
      extractVersionFromMarkdown(projectContext ?? '') ??
      'n/a',
    tech: packageJson ? inferTech(packageJson) : 'Unknown',
    features: dedupFeatures,
    git,
  }
}

function inferType(sourceKey, packageJson, defaultType) {
  if (sourceKey === 'Tool') {
    return packageJson?.main ? 'Desktop' : defaultType
  }
  return defaultType
}

export async function collectProjects() {
  const projectLists = await Promise.all(
    sourceDirs.map(async (source) => {
      let entries = []
      try {
        entries = await readdir(source.absolutePath, { withFileTypes: true })
      } catch {
        return []
      }

      const folders = entries
        .filter((entry) => entry.isDirectory())
        .filter((entry) => !entry.name.startsWith('.'))

      const projects = await Promise.all(
        folders.map(async (folder) => {
          const projectPath = path.join(source.absolutePath, folder.name)
          const metadata = await readProjectMetadata(projectPath, folder.name)
          return {
            id: normalizeId(source.key, folder.name),
            name: folder.name,
            source: source.key,
            localPath: projectPath,
            type: inferType(source.key, metadata.packageJson, source.defaultType),
            status: metadata.git.isRepo ? 'repo' : 'detected',
            version: metadata.version,
            tech: metadata.tech,
            github: metadata.git.remote,
            git: metadata.git,
            features: metadata.features,
          }
        }),
      )

      return projects
    }),
  )

  return projectLists.flat().sort((a, b) => a.name.localeCompare(b.name))
}

export async function writeProjectSnapshot() {
  const projects = await collectProjects()
  const payload = {
    generatedAt: new Date().toISOString(),
    total: projects.length,
    projects,
  }
  await mkdir(path.dirname(outputPath), { recursive: true })
  await writeFile(outputPath, `${JSON.stringify(payload, null, 2)}\n`, 'utf-8')
  return payload
}

export async function runGitAction(projectPath, action) {
  const commandMap = {
    fetch: ['fetch', '--all', '--prune'],
    status: ['status', '--short', '--branch'],
    pull: ['pull', '--ff-only'],
  }
  const args = commandMap[action]
  if (!args) {
    throw new Error(`Unsupported git action: ${action}`)
  }

  const output = await runGit(projectPath, args)
  if (output === null) {
    throw new Error('Git command failed')
  }
  return output
}
